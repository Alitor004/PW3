package br.com.motorola.model;

public class Telefone {
	
	private String gomesModelo;
	private int gomesPeso;
	
	public String getGomesModelo() {
		return gomesModelo;
	}
	public void setGomesModelo(String gomesModelo) {
		this.gomesModelo = gomesModelo;
	}
	public int getGomesPeso() {
		return gomesPeso;
	}
	public void setGomesPeso(int gomesPeso) {
		this.gomesPeso = gomesPeso;
	}
}
