package br.com.fabricadoce.model;

public class Doce {
	
	private String nomeDoce;
	private double pesoAcucar;
	
	public String getNomeDoce() {
		return nomeDoce;
	}
	public void setNomeDoce(String nomeDoce) {
		this.nomeDoce = nomeDoce;
	}
	public double getPesoAcucar() {
		return pesoAcucar;
	}
	public void setPesoAcucar(double pesoAcucar) {
		this.pesoAcucar = pesoAcucar;
	}
	
	
}
